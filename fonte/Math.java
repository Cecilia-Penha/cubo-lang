package fonte;
//Operações básicas

public class Math {
    public void add(double x, double y){
        double xy = x + y;
        System.out.println(xy);
    }

    public void sub(double x, double y){
        double xy = x - y;
        System.out.println(xy);
    }

    public void mult(double x, double y){
        double xy = x * y;
        System.out.println(xy);
    }

    public void div(double x, double y){
        double xy = x / y;
        System.out.println(xy);
    }
    
    public void mod(double x, double y){
        double xy = x % y;
        System.out.println(xy);
    }
}
