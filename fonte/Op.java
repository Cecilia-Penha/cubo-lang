package fonte;
//Operações básicas

public class Op {
    public double add(double x, double y){
        double xy = x + y;
        return(xy);
    }

    public double sub(double x, double y){
        double xy = x - y;
        return(xy);
    }

    public double mult(double x, double y){
        double xy = x * y;
        return(xy);
    }

    public double div(double x, double y){
        double xy = x / y;
        return(xy);
    }
    
    public double mod(double x, double y){
        double xy = x % y;
        return(xy);
    }
}
