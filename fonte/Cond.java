package fonte;

public class Cond {
    public void igualD(double a, double b){
        if (a == b){
            System.out.println("igual");
        }
        else{
            System.out.println("diferente");
        }
    }
    public void igualS(String a, String b){
        if (a == b){
            System.out.println("igual");
        }
        else{
            System.out.println("diferente");
        }
    }
}
